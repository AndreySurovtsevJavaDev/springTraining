// Аннотации конфигурации:
//      @Configuration - так мы определяем в проекте класс конфигурации. Это специальный класс, посредством которого можно настройить фреймворк на выполнение
//      определённых действий: создание бинов или активацию определённого функционала.
//
//      @ComponentScan - настраивает автоматическое сканирование компонентов
//      @Import - импортирует дополнительные конфигурации
//      @PropertySource - добавляет файл свойств в среду Spring

// Аннотации для внедрения зависимостей:
//      @Autowired - автоматическое внедрение зависимости
//      @Qualifier - уточняет, какой именно bean нужно внедрить
//      @Primary - отмечает предпочитаемый bean при выборе между несколькими вариантами
//      @Value - внедряет значение свойства или выражение SpEL

// Аннотации стереотипов компонентов:
//      @Component - общий стереотип для любого компонента Spring
//      @Service - специализированный компонент сервисного уровня
//      @Repository - специализированный компонент уровня доступа к данным
//      @Controller - специализированный компонент уровня представления

// Аннотации для веб-приложений:
//      @RequestMapping - сопоставление URL с методами контроллера
//      @GetMapping, @PostMapping и т.д. - специализированные версии @RequestMapping
//      @PathVariable - связывание параметра метода с частью URI
//      @RequestParam - связывание параметра метода с параметром запроса
//      @RequestBody - связывание тела запроса с параметром метода
//      @ResponseBody - указывает, что возвращаемое значение должно быть записано в тело ответа

// Аннотации AOP:
//      @Aspect - объявляет аспект
//      @Before, @After, @Around - типы советов (advice)
//      @Pointcut - определяет точку среза

// Аннотации транзакций:
//      @Transactional - указывает, что метод должен выполняться в транзакции

